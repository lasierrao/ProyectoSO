        private void btn_mos_jug_Click(object sender, EventArgs e)
        {
            string mensaje = "13/entrar";
            byte[] msg = System.Text.Encoding.ASCII.GetBytes(mensaje);
            server.Send(msg);

            //Recibimos la respuesta del servidor
            byte[] msg2 = new byte[80];
